# 4. Пользователь вводит целое положительное число. Найдите самую большую цифру в числе. Для решения используйте цикл while и
# арифметические операции.

a = input("Введите целое положительное число: ")
a = int(a)

if a <= 0:
    print("Введенное число не является положительным")
else:
    modulo = []
    while a >= 10:
        b = a % 10
        modulo.append(b)
        a = a // 10

print(f'Самая большая цифра введенного числа {max(modulo)}')

