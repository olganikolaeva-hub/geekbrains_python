# 6. Спортсмен занимается ежедневными пробежками. В первый день его результат составил a километров. Каждый день спортсмен
# увеличивал результат на 10 % относительно предыдущего. Требуется определить номер дня, на который общий результат спортсмена
# составить не менее b километров. Программа должна принимать значения параметров a и b и выводить одно натуральное число —
# номер дня.

a = input("Введите количество километров, которые пробежал спортсмен в первый день своей пробежки: ")
b = input(
    "Введите количество километров, которые должен пробежать спортсмен через N дней, увеличивая результат на 10% каждый день: ")

a = float(a)
b = float(b)

n = 1
c = (1 + 0.1)

while a * (c ** n) < b:
    n = n + 1

print(f'Спортсмен достигнет этого результата через {n + 1} дней')
